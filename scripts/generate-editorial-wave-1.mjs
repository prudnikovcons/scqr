const ensureDir = async (dir) => fs.mkdir(dir, { recursive: true });

const main = async () => {
	await Promise.all([ensureDir(POSTS_DIR), ensureDir(HERO_DIR), ensureDir(GRAPHICS_DIR)]);

	await Promise.all(
		posts.map((post, index) =>
			Promise.all([
				fs.writeFile(path.join(POSTS_DIR, `${post.slug}.md`), frontmatter(post), 'utf8'),
				fs.writeFile(path.join(HERO_DIR, `${post.slug}-hero.svg`), heroSvg(post, index), 'utf8'),
			])
		)
	);

	await Promise.all(
		Object.entries(graphics).map(([filename, source]) =>
			fs.writeFile(path.join(GRAPHICS_DIR, filename), source, 'utf8')
		)
	);

	await fs.writeFile(DOC_PATH, matrixDoc(posts), 'utf8');
	console.log(`Generated ${posts.length} editorial posts, ${posts.length} heroes and ${Object.keys(graphics).length} graphics.`);
};

main().catch((error) => {
	console.error(error);
	process.exitCode = 1;
});
